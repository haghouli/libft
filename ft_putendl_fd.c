#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	if (s == 0)
		return ;
	while (i < ft_strlen(s))
		write(fd, &s[i++], 1);
	write(fd, "\n", 1);
}
